/**
 * TIPOS PRIMITIVOS
 * ================
 * 
 *  -> number, string, boolean
 *  -> null, undefined
 * 
 */

// numéricos
let edad: number;

// string
let nombre: string;

// boolean
let esHombre: boolean;


//como usarlos
edad = 20;
nombre = "Victor";
esHombre = true;

// el tipo de datos null: significa la ausencia intencional de cualquier valor o referencia a un objeto.
// es un valor que se le asigna deliberadamente a una variable para indicar que "no hay valor".
// -- es algo que existe y se le ha asignado explícitamente "nada"
// 
let profesion: string | null;
profesion = null;

// UNDEFINED: una variable ha sido declarada pero todavía no se le ha asignado un valor.
// -- es algo que existe, pero está vacío o no ha sido inicializado
let pais; // por ahora esta undefined | indefinido

